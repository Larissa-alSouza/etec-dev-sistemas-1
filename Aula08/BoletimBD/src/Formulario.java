    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        
        if(jtfRA.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha o R.A.", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfAluno.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha o nome do aluno", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfMateria.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha o nome da matéria", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfB1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha a nota B1", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfB2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha a nota B2", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfB3.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha a nota B3", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        else if(jtfB4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha a nota B4", "Confirmação",
            JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        double media = (Double.parseDouble(jtfB1.getText()) + Double.parseDouble(jtfB2.getText()) + Double.parseDouble(jtfB3.getText()) + Double.parseDouble(jtfB4.getText()) ) / 4;
        
        try
        {
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://127.0.0.1/banco", "root", "");
            Statement stmt=(Statement)con.createStatement();
            
            String insert="INSERT INTO boletim VALUES('"+jtfRA.getText()+"','"+jtfMateria.getText()+"', '"+jtfB1.getText()+"', '"+jtfB2.getText()+"', '"+jtfB3.getText()+"', '"+jtfB4.getText()+"', '"+media+"');";
            stmt.executeUpdate(insert);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", 1);
        }
        this.Lista();
        
        jtfRA.setText("");
        jtfAluno.setText("");
        jtfMateria.setText("");
        jtfB1.setText("");
        jtfB2.setText("");
        jtfB3.setText("");
        jtfB4.setText("");
    }//GEN-LAST:event_btnGravarActionPerformed

