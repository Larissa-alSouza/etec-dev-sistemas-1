    private void Lista() {
        
        try
        {
            Connection con=(Connection)DriverManager.getConnection("jdbc:mysql://127.0.0.1/banco", "root", "");
            String sql;
            sql = "select * from boletim";
            PreparedStatement banco = (PreparedStatement)con.prepareStatement(sql);
            banco.execute();
            ResultSet resultado = banco.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            while(resultado.next())
            {
                model.addRow(new Object[]
                {
                    resultado.getString("ra"),
                    resultado.getString("materia"),
                    resultado.getString("b1"),
                    resultado.getString("b2"),
                    resultado.getString("b3"),
                    resultado.getString("b4"),
                    resultado.getString("media")
                });
            }
            
            banco.close();
            con.close();       
        }
        catch(SQLException ex)
        {
            System.out.println("O erro foi: " + ex);
        }
    }

